function t(key) {
  const lang = sessionStorage.getItem('lang') || 'es';
  return TRANSLATIONS[lang]?.[key] ?? key;
}

function getLang() {
  return sessionStorage.getItem('lang') || 'es';
}

function setLang(lang) {
  sessionStorage.setItem('lang', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('lang-btn-active', btn.dataset.lang === lang);
  });
  applyTranslations();
}

function applyTranslations() {

  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });

  const pageKey = document.body.dataset.page;
  if (pageKey) {
    const titleMap = {
      dashboard:     'nav.dashboard',
      fichajes:      'nav.fichajes',
      solicitudes:   'nav.solicitudes',
      incidencias:   'nav.incidencias',
      empleados:     'nav.empleados',
      sedes:         'nav.sedes',
      turnos:        'nav.turnos',
      informes:      'nav.informes',
      configuracion: 'nav.configuracion',
    };
    if (titleMap[pageKey]) {
      document.title = `${t(titleMap[pageKey])} — D&D Control`;
    }
  }
}
